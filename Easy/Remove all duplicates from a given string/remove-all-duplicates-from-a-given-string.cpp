//User function template for C++
class Solution{
public:
	string removeDuplicates(string str) {
	    // code here
	    string ans;
	    unordered_set<char> set;
	    for(char c:str){
	        if(set.find(c)==set.end()){
	            set.insert(c);
	            ans.push_back(c);
	        }
	    }
	    return ans;
	}
};

